package org.bohdansharubin.models;

/**
 * Represents American clothing sizes.
 * <p>
 * The sizes follow the standard US letter-based sizing system,
 * ranging from Small (S) to Triple Extra Large (XXXL).
 */
public enum AmericanSize {

    /** Small size */
    S,

    /** Medium size */
    M,

    /** Large size */
    L,

    /** Extra Large size */
    XL,

    /** Double Extra Large size */
    XXL,

    /** Triple Extra Large size */
    XXXL
}
